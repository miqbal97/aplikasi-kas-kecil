    private void get_data_table_filtered_by_date(){
        final String[] _label = {"Tanggal", "No Nota", "Keterangan", "Debit", "Kredit", "Sisa Saldo"};
        try {
            ResultSet result = db.runQuery("SELECT tanggal_pembentukan, no_pengisian, keterangan_periode, jumlah, terpakai, sisa_saldo "
                                         + "FROM pembentukan_dana WHERE tanggal_pembentukan BETWEEN '"+f_from.getText()+"' AND '"+f_to.getText()+"'");
            
            this._from = f_from.getText(); this._to = f_to.getText();
            
            ResultSetMetaData table = result.getMetaData();
            int _row = 0, counter = 0; while(result.next()){ _row = result.getRow(); }
            
            Object[][] data_nota = new Object[_row][table.getColumnCount()];
            result.beforeFirst();
            
            while(result.next()){
                for(int i = 0; i < table.getColumnCount(); i++){ data_nota[counter][i] = result.getString(i+1); }
                counter++;
            }
            
            t_data.setModel(new javax.swing.table.DefaultTableModel(data_nota, _label));
        } catch (SQLException err){ koneksi.print(err.getMessage()); }
    }
    
